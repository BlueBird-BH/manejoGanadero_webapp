package co.edu.unisabana.manejoganado;

public class Vaca {
    String codigo, nombre, edad, corral, codigoMadre;
    
    public Vaca() {}

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    public String getCorral() {
        return corral;
    }

    public void setCorral(String corral) {
        this.corral = corral;
    }

    public String getCodigoMadre() {
        return codigoMadre;
    }

    public void setCodigoMadre(String madre) {
        this.codigoMadre = madre;
    }
}
